package Datatype;

public class Datatype {
    public  static void main(String[] args){
        int x = 5;
        System.out.println("Integer Value is : " + x);
        char c = 'c';
        System.out.println("Char value is : " + c);
        float f = 2.3f;
        System.out.println("Float Value is : " + f);
        double d = 3.15d;
        System.out.println("Double Value is : " + d);

    }
}
