package Loop;

public class Loop {
    public void series(){
        for(int i=1;i<10;i++){
            System.out.println(i);

        }

        }
        public void serial(){
            int i=0;
            while(i<10){
                System.out.println(i);
                i++;
            }
    }
        public void killer(){
            int i=0;
            do{
                System.out.println(i);
                i++;

            } while(i<10);
        }






























}
